package com.hardik.hirapara;

/**
 * Box Class
 * 
 * @author HARDIK HIRAPARA
 *
 */
public class Box {

	public double width;
	public double height;
	public double depth;

	@Override
	public String toString() {
		return "Box [width=" + this.width + ", height=" + this.height + ", depth=" + this.depth + "]";
	}
}
